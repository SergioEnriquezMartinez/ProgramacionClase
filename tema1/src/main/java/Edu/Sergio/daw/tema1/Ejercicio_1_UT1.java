package Edu.Sergio.daw.tema1;

public class Ejercicio_1_UT1 {
	public static void main (String[] args) {
		int dato1, dato2, suma, resta, producto, cociente;
		dato1 = 20;
		dato2 = 10;
		
		//Suma
		suma = dato1 + dato2;
		System.out.println(dato1 + "+" + dato2 + "=" + suma);
		
		//Resta
		resta = dato1 - dato2;
		System.out.println(dato1 + "-" + dato2 + "=" + resta);
		
		//Producto
		producto = dato1 * dato2;
		System.out.println(dato1 + "*" + dato2 + "=" + producto);
		
		//Cociente
		cociente = dato1 / dato2;
		System.out.println(dato1 + "/" + dato2 + "=" + cociente);
		
	}
	
}
